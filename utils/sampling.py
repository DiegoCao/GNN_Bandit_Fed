def binary_matrix(m,pad):
    bm = []
    for i, seq in enumerate(m):
        bm.append([])
        for token in seq:
            if token == pad:
                bm[i].append(0)
            else:
                bm[i].append(1)
    return bm


def superuser_noniid(dataset, vocab):
    def tokens_to_ids(raw_batch):
        def tokens_to_word_ids(tokens, vocab):
            return [vocab[word] for word in tokens]

        to_ret = [tokens_to_word_ids(seq, vocab) for seq in raw_batch]
        return to_ret

    for user, data in dataset.items():
        data_x = data['x']*10
        data_y = data['y']*10
        perm = np.random.permutation(len(data['x']))
        data_x_new = []
        data_y_new = []
        for i in perm:
            data_x_new.extend(tokens_to_ids(data_x[i]))
            data_y_new.extend(tokens_to_ids(data_y[i]['target_tokens']))
        mask = binary_matrix(data_y_new,pad=vocab['<pad>'])
        dataset[user] = {'x': data_x_new, 'y': data_y_new, 'mask': mask}
    return dataset


